/**
 * 三分钟一集（默认 12 段 × 15s ≈ 180s）内容质量硬锁：
 * 对白密度 / 道具入画 / 运镜轨迹 / 动作轨迹 / 场景渲染 /
 * 多动作·人物互动·天气突变·对白转打斗等场面变化。
 * 只写可拍约束，前台零技术泄漏。
 */

/** 对白密度：三分钟集须够密，但不能灌水 */
export const MANHUA_DIALOGUE_DENSITY_BLOCK = `【对白密度·三分钟集】
- 整集目标约 180 秒（约 12 段），对白须推动关系或信息，禁止闲聊天气/寒暄灌水。
- 密度：平均每段至少 1 句可表演对白；关键冲突段 2–3 句短对白；整集建议 12–20 句有效对白（一句一拳）。
- 对白功能：揭露 / 施压 / 误导 / 反转 / 关系裂缝——每句至少完成一类。
- 表演：台词只驱动口型、气口与微表情；画面零字幕、零气泡。
- 若本段将转入打斗/追逐：对白须在爆发前收束（半句、打断、被动作盖过），禁止边打边长篇说教。`;

/** 道具入画：信息载体，非装饰 */
export const MANHUA_PROP_IN_FRAME_BLOCK = `【道具入画硬锁】
- 本集点选/编剧包道具必须在至少 2 个段的关键静帧与成片中可读入画（手持、递接、特写、桌面落点）。
- 道具须承载信息（信物/凭证/凶器暗示物/身份标记等），禁止纯装饰摆件。
- 递接镜：给出→接住→反应，三段动作轨迹写清；特写镜须让道具占画面主体。
- 场面升级时（对白→冲突）：道具可成为导火索或武器化暗示（夺过、摔下、挡格），须写清接触点；禁止道具瞬移失踪。
- 静帧与成片服装/道具时代一致，禁止错时代穿戴。`;

/** 运镜轨迹：段内有主运镜，镜间可递进；场面升级时可换主运镜 */
export const MANHUA_CAMERA_TRAJECTORY_BLOCK = `【运镜轨迹】
- 每段成片主运镜以 1 种为主（推/拉/横移/跟随/环绕/手持微晃/过肩等），写清起幅→落幅。
- 段内约 4 张静帧：景别递进（如全景建立→中景关系→近景压力→特写兑现），禁止四镜同一机位摆拍。
- 场面性质变化时（对白→打斗、静站→追逐）：允许下一段换主运镜与节奏；须在上一段末镜用动作/视线/道具预示，禁止无因硬切。
- 路径运镜若已点选：按蓝轨镜头/红轨人物执行，成片不显示参考线。
- 禁止单镜堆叠甩镜+升降+手持；禁止无动机空镜游移。`;

/** 动作轨迹：允许多拍动作链 + 人物互动 */
export const MANHUA_ACTION_TRAJECTORY_BLOCK = `【动作轨迹·多拍互动】
- 同一场景/同一段可有多拍动作，不限于单一姿势：写清动作链（A→B→C），每拍含：谁、从哪到哪、起止姿态、接触点。
- 主角与其他人物互动必须可拍：对视轴线、站位远近、递接/推挡/拉扯/护住/逼近/退让；禁止旁人只当背景板。
- 段内动作链连续：上镜落点=下镜起幅，禁止瞬移换姿；多人场面须交代各自起止，避免穿模贴脸。
- 对白段可叠加小动作（握拳、偏头、挡路）；打斗/剧烈动作段须写清发力方向与收势，舞台化克制、无写实伤害表现。
- 情绪必须变成可见动作；成片微动落实关键过程，禁止纯呼吸空镜灌时长。`;

/** 场景渲染：空间 + 天气/氛围变化须有过渡 */
export const MANHUA_SCENE_RENDER_BLOCK = `【场景渲染·氛围可变压】
- 每段须交代可读空间：地点类型、内外/日夜、纵深层次（前景遮挡·中景人物·远景环境）。
- 场景设定图/点选场景的材质与主色调须可辨认延续；禁止无因跳棚。
- 天气/氛围突变（晴→雨、静→风、暖灯→冷光）必须可拍过渡：云影压暗、第一滴雨、湿石反光、发丝被风掀起、火把熄灭等；至少 1 镜专责「变化发生中」，禁止上一镜大晴下一镜已湿透且无中介。
- 转场须有门窗/台阶/廊柱等过渡物，或明确切点理由（门被撞开、帘掀起、雷声切）。
- 竖屏构图：主体与互动双方清晰；关键道具与表情不被裁切；布光服务情绪，禁止统一暖背景套模板。`;

/** 场面变化：对白↔打斗、节奏突变、同场多事件 */
export const MANHUA_SCENE_DYNAMICS_BLOCK = `【场面变化·同场多事件】
- 同一场景可以不只一种事件：对白施压 → 信息揭露 → 肢体冲突 → 追逐离开；须按节拍拆开写，每拍有信息增量。
- 主角与配角/对手的互动层级要升级：目光→台词→道具接触→身体接触/对峙走位；禁止全程各说各话零接触。
- 「对白突然变剧烈打斗」：须有引爆点（一句话、一个动作、道具被夺/落地、第三者闯入）；爆发前半拍给预兆（手伸向刀柄、后退贴墙、呼吸变促）。
- 打斗/剧烈场面仍遵守：主运镜一事、动作轨迹清楚、舞台化克制；结束后可回到对白或钩子，勿无限缠斗灌时长。
- 天气/昼夜变化可与情节同步（雨落下的瞬间证据被洗掉、雷声盖过半句对白），变化须服务戏剧，不做空氛围。
- 成片段内若含性质跳变：前半对齐对白静帧节奏，后半对齐动作静帧节奏，中间用 1 个可见转折动作接上。`;

/** 拼进故事/节拍/反推的总块 */
export function composeManhuaEpisodeQualityBlock(opts?: {
  includeDialogue?: boolean;
  includeProp?: boolean;
  includeCamera?: boolean;
  includeAction?: boolean;
  includeScene?: boolean;
  includeDynamics?: boolean;
}): string {
  const o = {
    includeDialogue: true,
    includeProp: true,
    includeCamera: true,
    includeAction: true,
    includeScene: true,
    includeDynamics: true,
    ...opts,
  };
  return [
    o.includeDialogue ? MANHUA_DIALOGUE_DENSITY_BLOCK : "",
    o.includeProp ? MANHUA_PROP_IN_FRAME_BLOCK : "",
    o.includeCamera ? MANHUA_CAMERA_TRAJECTORY_BLOCK : "",
    o.includeAction ? MANHUA_ACTION_TRAJECTORY_BLOCK : "",
    o.includeScene ? MANHUA_SCENE_RENDER_BLOCK : "",
    o.includeDynamics ? MANHUA_SCENE_DYNAMICS_BLOCK : "",
  ]
    .filter(Boolean)
    .join("\n\n");
}
