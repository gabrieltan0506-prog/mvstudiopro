/** 创作顾问 system 末尾可附加的软参考（短） */
export function composeDistilledAdvisorSoftBlock(question: string, opts?: { canvasManhua?: boolean }): string {
  const q = String(question || "");
  const parts: string[] = [];
  if (
    opts?.canvasManhua ||
    /分镜|静帧|漫剧|编剧|古装|CG|画风|关键帧|镜头轨|视觉简报/.test(q)
  ) {
    parts.push(DISTILLED_MANHUA_VISUAL_NARRATIVE_ZH);
  }
  if (
    /抖音|快手|B站|哔哩|小红书|红书|短视频|剪辑|完播|老铁|弹幕|UP主|投流|加热/.test(q) ||
    (!opts?.canvasManhua && /选题|文案|钩子|封面|带货|直播/.test(q))
  ) {
    parts.push(DISTILLED_CHINA_SHORT_VIDEO_GROWTH_ZH);
  }
  if (!parts.length && opts?.canvasManhua) {
    parts.push(DISTILLED_MANHUA_VISUAL_NARRATIVE_ZH);
  }
  return parts.join("\n\n");
}
