/** 知识付费型人设：变现路径约束（医疗/文化审美等） */
export function buildKnowledgeMonetizationConstraint(context: string): string {
  const s = String(context || "");
  const knowledgeLike =
    /医生|医师|医疗|临床|医学|文化|艺术|历史|书画|收藏|人文|学者|教授|博士|lawyer|律师|顾问/i.test(s);
  if (!knowledgeLike) return "";
  return `\n\n特别约束：此用户偏专业/审美/知识型人设。monetizationLanes 中禁止出现电商带货路径。变现路径只能包含：知识付费（课程/私人咨询或深度交流）、专业背书型品牌合作、机构讲座/合作、高端审美内容服务。`;
}
